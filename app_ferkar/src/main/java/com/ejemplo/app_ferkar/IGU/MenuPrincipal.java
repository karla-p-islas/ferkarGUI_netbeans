    private void Button_II_TerminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Button_II_TerminarActionPerformed
        String TratamientoA = "";
        if(!CheckBox_II_Galvanizado.isSelected() && !CheckBox_II_Pintado.isSelected() && !CheckBox_II_Reforzado.isSelected()){
            TratamientoA = "NADA";
        }else{
            if(CheckBox_II_Galvanizado.isSelected()){
                TratamientoA += "G";
            }
            if(CheckBox_II_Pintado.isSelected()){
                TratamientoA += "P";
            }
            if(CheckBox_II_Reforzado.isSelected()){
                TratamientoA += "R";
            }
        }
        
        if(!"".equals(jText_II_ClaveAro.getText()) && !"".equals(jText_II_NombreSoldador.getText()) && !"".equals(jText_II_Cantidad.getText()) && !"".equals(jText_II_Folio.getText())){
            ii.setFolio(jText_II_Folio.getText());
            ii.setFecha(jFormText_II_Fecha.getText());
            ii.setId_soldador(Integer.parseInt(jText_II_IDSoldador.getText()));
            ii.setCaseta(Integer.parseInt(jText_II_NumCaseta.getText()));
            ii.setHora_inicio(jFormattedText_II_HoraInicio.getText());
            ii.setHora_fin(jFormattedText_II_HoraFin.getText());
            ii.setCodigo_aro(jText_II_ClaveAro.getText());
            ii.setTratamiento_adicional(TratamientoA);
            ii.setCantidad(Integer.parseInt(jText_II_Cantidad.getText()));
            String ubi = (String) jBox_ubicacion.getSelectedItem();
            ii.setUbicacion(ubi);
            int clave = Integer.parseInt(jText_II_ClaveAro.getText());
            int cantidad = Integer.parseInt(jText_II_Cantidad.getText()); 
            int resultado = CantidadAtados(clave,cantidad);
            
            //agregar aqui la actualización de inventario general
            inv.setCodigo_aros(jText_II_ClaveAro.getText());
            inv.setTrato_adicional(TratamientoA);
            inv.setAros(Integer.parseInt(jText_II_Cantidad.getText()));
            
            if(resultado == -1){
                JOptionPane.showMessageDialog(null, "No se puede registrar: cantidad atados inválida");
            }else{
                ii.setCantidad_atados(resultado);
                ii.setCantidad_exs(resultado);
                inv.setAtados(resultado);
                iid.RegistrarInventario(ii);    //registra en la tabla "produccion_diaria"
                invd.Existencias(inv);          //registra en la tabla "existencia_aros"
                JOptionPane.showMessageDialog(null, "Folio Registrado");
            }
            
            //limpiar
            CheckBox_II_Galvanizado.setSelected(false);
            CheckBox_II_Pintado.setSelected(false);
            CheckBox_II_Reforzado.setSelected(false);
            jFormText_II_Fecha.setText("");
            jFormattedText_II_HoraFin.setText("");
            jFormattedText_II_HoraInicio.setText("");
            jText_II_Cantidad.setText("");
            jText_II_ClaveAro.setText("");
            jText_II_IDSoldador.setText("");
            jText_II_NombreSoldador.setText("");
            jText_II_NumCaseta.setText("");
            jText_II_TipoAro.setText("");
            jText_II_Folio.setText("");
            jBox_ubicacion.setSelectedIndex(0);
        }else{
            JOptionPane.showMessageDialog(null, "Datos incorrectos o incompletos");
        }
    }//GEN-LAST:event_Button_II_TerminarActionPerformed

