    public static int CantidadAtados(int clave, int cantidad){
        int ultimoNum = clave %10;
        int divisor;
        
        switch(ultimoNum){
            case 4 -> divisor = 25;
            case 8 -> divisor = 20;
            case 5 -> divisor = 10;
            default -> {
                return -1;
            }
        }
         if(cantidad % divisor == 0){
             return cantidad/divisor;
         }else{
             JOptionPane.showMessageDialog(null, "Error en clave o atado incompleto");
             return -1;
         }
    }
    
